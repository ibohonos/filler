#include "filler.h"

void	ft_find_player(char *line, t_map *p)
{
	if (line[10] == '1')
	{
		p->player = 'O';
		p->enemy = 'X';
	}
	else if (line[10] == '2')
	{
		p->player = 'X';
		p->enemy = 'O';
	}
}
