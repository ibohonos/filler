#include "filler.h"

int		ft_find_spase(t_map *p, int i, int j)
{
	int k;

	k = 0;
	if ((i - 2) >= 0 && p->map[i - 1][j] == '.' && p->map[i - 2][j] == '.')
		k++;
	if ((i + 2) < p->h && p->map[i + 1][j] == '.' && p->map[i + 2][j] == '.')
		k++;
	if ((j - 2) >= 0 && p->map[i][j - 1] == '.' && p->map[i][j - 2] == '.')
		k++;
	if ((j + 2) < p->w && p->map[i][j + 1] == '.' && p->map[i][j + 2] == '.')
		k++;
	if (k >= 2)
		return (1);
	return (0);
}
